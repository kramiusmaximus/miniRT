#include "libft.h"

t_list	*ft_lstget(t_list *lst, int index)
{
	if (!lst)
		return (NULL);
	while (lst)
	{
		if (index-- == 0)
			return (lst);
		lst = lst->next;
	}
	return (NULL);
}
