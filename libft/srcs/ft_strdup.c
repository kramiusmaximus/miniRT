#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*copy;
	int		dstsize;

	dstsize = ft_strlen(s) + 1;
	copy = malloc(dstsize * sizeof(char));
	if (copy)
		ft_strlcpy(copy, s, dstsize);
	return (copy);
}
