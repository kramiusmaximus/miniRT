#include "libft.h"

int	ft_atoi(const char *s)
{
	int sign;
	int res;

	sign = 1;
	res = 0;
	while (*s == ' ' || *s == '\t' || *s == '\n'
	|| *s == '\v' || *s == '\r' || *s == '\f')
		s++;
	if (*s == '+' || *s == '-')
	{
		if (*s == '-')
			sign *= -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		if (res * 10 < res)
			return (sign == 1 ? -1 : 0);
		res = (res * 10) + (*s - '0');
		s++;
	}
	return (sign * res);
}
